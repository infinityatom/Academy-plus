#include <stdlib.h>

wchar_t	*ft_itoa(int n)
{
	size_t	size_ret;
	wchar_t	*ret;
	wchar_t	sign;
	int		temp_n;

	sign = (n < 0) ? -1 : 1;
	size_ret = 2 + (n < 0);
	temp_n = n;
	while ((n = n / 10))
		size_ret++;
	n = temp_n;
	if ((ret = (wchar_t *)malloc(sizeof(wchar_t) * size_ret--)) == NULL)
		return (NULL);
	ret[size_ret--] = '\0';
	ret[size_ret--] = sign * (n % 10) + '0';
	while ((n = n / 10))
		ret[size_ret--] = sign * (n % 10) + '0';
	if (sign < 0)
		ret[size_ret] = '-';
	return (ret);
}
